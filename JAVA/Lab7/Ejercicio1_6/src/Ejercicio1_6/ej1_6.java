    private void getBoxInfo()
    {
        try {
            File directorioOX = new File("./imagenes");
            File directorioWN = new File(".\\imagenes");
            if (directorioOX.exists() && directorioOX.isDirectory()) 
            {
                OS=true;
                String listado[] = directorioOX.list();
                int longitud =listado.length;
                marcas.add("--SELECCIONE UNA IMAGEN--"); 
                for (int x=0;x<longitud;x++) 
                {                    
                    if (listado[x].charAt(0)!='.')
                        marcas.add(listado[x]);                                          
                }
                Box = new JComboBox(marcas.toArray());
            }
            else if (directorioWN.exists() && directorioWN.isDirectory()) 
            {
                OS=false;
                String listado[] = directorioWN.list();
                int longitud =listado.length;
                marcas.add("--SELECCIONE UNA IMAGEN--"); 
                for (int x=0;x<longitud;x++) 
                {                    
                    if (listado[x].charAt(0)!='.')
                        marcas.add(listado[x]);                                          
                }
                Box = new JComboBox(marcas.toArray());
            } 

        } catch (Exception e) {
            System.out.println(e.toString());
        }
    }

