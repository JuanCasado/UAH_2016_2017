    private double saldoMaximo()
    {
        double maximo = jugador[0].getSaldo();
        for (int x =1;x<3;x++)
        {
            if (jugador[x].getSaldo()>maximo)
                maximo = jugador[x].getSaldo();
        }
        return maximo;
    }
    
